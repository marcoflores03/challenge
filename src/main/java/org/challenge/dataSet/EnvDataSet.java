package org.challenge.dataSet;

public class EnvDataSet extends DataSet {

    private String url;
    private String username;
    private String password;

    public EnvDataSet() {
        super();
    }

    public String getUrl() {
        return url;
    }
    public String getUsername() {
        return username;
    }
    public String getPassword() {
        return password;
    }
}